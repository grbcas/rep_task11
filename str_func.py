def upper(in_str: str) -> str:
	"""
	
	upper(in_str: str) -> str.upper()
	
	"""
	return in_str.upper()


def capitalize_str(in_str: str) -> str:
	"""

	capitalize_str(in_str: str) -> str.capitalize()

	"""
	return in_str.capitalize()


if __name__ == '__main__':
	print(upper('text'))
	print(capitalize_str('test'))
